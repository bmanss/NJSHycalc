export const reforgeList = (() => {
  const specificArmorReforges = ["loving", "ridiculous"];
  const equipmentReforges = [""].concat(Object.keys(reforges.equipment));
  const swordReforges = [""].concat(Object.keys(reforges.sword));
  const bowReforges = [""].concat(Object.keys(reforges.bow));

  const allArmorReforges = Object.entries(reforges.armor).reduce((result, [reforge, values]) => {
    if (!specificArmorReforges.includes(reforge)) {
      result.push(reforge);
    }
    return result;
  }, []);

  allArmorReforges.push("");

  const chestplateReforges = [...allArmorReforges, "loving"];
  const helmetReforges = [...allArmorReforges, "ridiculous"];
  allArmorReforges.sort((a, b) => a.localeCompare(b));
  chestplateReforges.sort((a, b) => a.localeCompare(b));
  helmetReforges.sort((a, b) => a.localeCompare(b));

  return {
    generalArmor: allArmorReforges,
    chestplate: chestplateReforges,
    helmet: helmetReforges,
    equipment: equipmentReforges,
    sword: swordReforges,
    bow: bowReforges,
  };
})();
